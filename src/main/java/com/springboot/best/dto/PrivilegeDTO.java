package com.springboot.best.dto;

import java.io.Serializable;

public class PrivilegeDTO implements Serializable{

	private Long id;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	private String name;
	
}
